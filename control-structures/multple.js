// if...else Statement: This structure allows you to execute one block of code if the condition is true, and another if it's false.

if (grade >= 50) {
    console.log("You passed!");
} else {
    console.log("Try again.");
}

if (isRaining) {
    console.log("Take an umbrella.");
} else {
    console.log("Enjoy the sun!");
}

if (temperature < 20) {
    console.log("Wear a jacket.");
} else {
    console.log("It's a warm day, no jacket needed.");
}

if (userRole === 'admin') {
    console.log("Access granted.");
} else {
    console.log("Access denied.");
}

if (trafficLight === 'green') {
    console.log("Go.");
} else {
    console.log("Stop.");
}
